#!/usr/bin/env node
// sync-areas-from-supabase.mjs — DB → repo materialiser (the permanent anti-drift fix).
//
// WHY THIS EXISTS
// The coordinate drift this tool prevents was caused by a one-way contract: only
// repo → Supabase was automated (sync-content-to-supabase.mjs), so a DB-side edit
// (e.g. an id/postcode migration applied via MCP) had no automated way back into the
// repo and the two stores diverged. Per the owner's decision the Supabase `areas`
// mirror is now the SOURCE OF TRUTH for area records (CLAUDE.md §18.5, relaxed for
// `areas` only), and `data/areas/<id>.json` is a MATERIALISED VIEW regenerated by
// this tool. There is now exactly ONE automated write path in each direction.
//
// WHAT IT DOES (idempotent — writes only on change):
//   • reads every areas row (id + data jsonb) from the DB,
//   • writes each to data/areas/<id>.json, projected through area-fields.mjs so the
//     shape/field-order matches tools/build-areas.mjs (no thrash when build runs next),
//   • with --prune, removes any per-area file whose id no longer exists in the DB
//     (this is how an id migration's stale file — e.g. lake-sp3 → lake-sp4 — is cleared),
//   • writes a compact parity snapshot to data/snapshots/areas.json
//     ({id,name,postcode,coords,coordsSource,active}) that the OFFLINE parity test
//     (tests/areas-db-repo-parity.test.js) checks every per-area file against — this is
//     the regression that makes future hand-drift fail CI.
//
// TWO READ MODES (mirrors tools/backfill-geofence.mjs):
//   File mode (this sandbox; the dump is produced via the Supabase MCP connector):
//     # dump via MCP execute_sql:  SELECT id, data FROM areas ORDER BY id;  -> /tmp/areas-dump.json
//     node tools/sync-areas-from-supabase.mjs --from-file /tmp/areas-dump.json --prune
//   REST mode (CI / a machine with the service-role key):
//     SUPABASE_URL + SUPABASE_SERVICE_ROLE_KEY set → read via PostgREST.
//     node tools/sync-areas-from-supabase.mjs --prune
//
//   --dry-run   report what would change; write nothing.
//   --no-prune  (default) never delete a per-area file even if absent from the dump.
//
// AFTER running this, ALWAYS: node tools/build-areas.mjs (rebuild the index) →
// node tools/verify-area-coords.mjs --online → node tools/run-all-tests.mjs.

import { readFile, readdir, writeFile, unlink, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { DETAIL_FIELDS } from './area-fields.mjs';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const AREAS_DIR = resolve(root, 'data/areas');
const SNAPSHOT = resolve(root, 'data/snapshots/areas.json');
const SUPABASE_URL = (process.env.SUPABASE_URL || 'https://qxmyrahqsopmaeokxdub.supabase.co').replace(/\/$/, '');
const SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY || '';

// Per-area file shape defaults — mirrors tools/build-areas.mjs so a sync followed by a
// build-areas run produces byte-identical per-area files (the index is build's job).
function canonicalRecord(data) {
  const p = data || {};
  const prices = p.prices ?? {};
  const a = {
    id: p.id,
    name: p.name ?? p.village,
    village: p.village,
    town: p.town,
    county: p.county,
    postcode: p.postcode,
    hubCity: p.hubCity ?? '',
    regionDir: p.regionDir ?? '',
    settlementType: p.settlementType ?? '',
    subRegion: p.subRegion ?? p.town,
    coords: p.coords ?? null,
    coordsSource: p.coordsSource ?? null,
    overview: p.overview ?? '',
    character: p.character ?? '',
    amenities: p.amenities ?? [],
    schools: p.schools ?? [],
    transport: p.transport ?? { commutes: [] },
    prices,
    priceSummary: p.priceSummary ?? null,
    thingsToDo: p.thingsToDo ?? [],
    placesToEat: p.placesToEat ?? [],
    pros: p.pros ?? [],
    cons: p.cons ?? [],
    whoItSuits: p.whoItSuits ?? [],
    councilTaxBand: p.councilTaxBand ?? null,
    broadbandMedianMbps: p.broadbandMedianMbps ?? null,
    nearestStation: p.nearestStation ?? null,
    primarySupermarket: p.primarySupermarket ?? null,
    houseTypeIds: p.houseTypeIds ?? [],
    images: p.images ?? [],
    sources: p.sources ?? [],
    status: p.status ?? 'directory',
    ...(p.rightmove != null ? { rightmove: p.rightmove } : {}),
    ...(p.geofenceRadiusMi != null ? { geofenceRadiusMi: p.geofenceRadiusMi } : {}),
    ...(p.searchRadiusMi != null ? { searchRadiusMi: p.searchRadiusMi } : {}),
    ...(p.active != null ? { active: p.active } : {}),
  };
  // Project through DETAIL_FIELDS (same key set + order as build-areas' pick()).
  const out = {};
  for (const k of DETAIL_FIELDS) if (k in a) out[k] = a[k];
  return out;
}

/** Compact parity-snapshot row — the four coordinate-truth fields the parity test guards. */
export function snapshotRow(data) {
  const p = data || {};
  return {
    id: p.id,
    name: p.name ?? p.village ?? null,
    postcode: p.postcode ?? null,
    coords: p.coords ?? null,
    coordsSource: p.coordsSource ?? null,
    active: p.active === false ? false : true,
  };
}

/** Normalise a dump (array of {id,data} rows, or {rows:[…]}) into [{id, data}]. */
export function normaliseDump(raw) {
  const rows = Array.isArray(raw) ? raw : Array.isArray(raw?.rows) ? raw.rows : null;
  if (!rows) throw new Error('dump is not an array of {id,data} rows');
  return rows.map((r) => {
    const id = r.id ?? r.data?.id;
    let data = r.data ?? r;
    if (typeof data === 'string') data = JSON.parse(data);
    if (!id) throw new Error('dump row missing id');
    if (data.id && data.id !== id) {
      throw new Error(`dump row id mismatch: column id="${id}" but data.id="${data.id}" — DB inconsistency, fix the DB first`);
    }
    return { id, data: { ...data, id } };
  });
}

const json = (v) => JSON.stringify(v, null, 2) + '\n';

// Order-insensitive deep equality. Postgres jsonb does not preserve key order, so a
// freshly-pulled row is the SAME record as a hand-authored file even when nested keys
// sit in a different order. We treat those as unchanged and KEEP the existing file —
// the materialised view is semantic, not byte-for-byte — so a sync produces a minimal,
// reviewable diff (only genuine value changes) instead of churning nested key order.
export function canonEqual(a, b) {
  const canon = (v) => {
    if (Array.isArray(v)) return v.map(canon);
    if (v && typeof v === 'object') {
      const o = {};
      // A null-valued key and an absent key mean the same thing in this data model
      // (no value). Drop nulls so an older file that omits null optionals is treated
      // as equal to a freshly-projected row that spells them out — no spurious rewrite.
      for (const k of Object.keys(v).sort()) { if (v[k] === null) continue; o[k] = canon(v[k]); }
      return o;
    }
    return v;
  };
  return JSON.stringify(canon(a)) === JSON.stringify(canon(b));
}

async function readDiskIds() {
  const files = (await readdir(AREAS_DIR)).filter((f) => f.endsWith('.json'));
  return files.map((f) => f.replace(/\.json$/, ''));
}

async function restGetAreas() {
  const out = [];
  const PAGE = 1000;
  for (let from = 0; ; from += PAGE) {
    const url = `${SUPABASE_URL}/rest/v1/areas?select=id,data`;
    const res = await fetch(url, {
      headers: { apikey: SERVICE_KEY, Authorization: `Bearer ${SERVICE_KEY}`, Range: `${from}-${from + PAGE - 1}` },
    });
    if (!res.ok) throw new Error(`GET areas failed: ${res.status} ${await res.text()}`);
    const rows = await res.json();
    out.push(...rows);
    if (rows.length < PAGE) break;
  }
  return out;
}

// ── plan + apply (pure-ish; exported for tests) ───────────────────────────────
export async function materialise(rows, { prune = false, dryRun = false } = {}) {
  await mkdir(AREAS_DIR, { recursive: true });
  // Skip household-onboarding stubs (Phase 2): rows the onboarding wizard creates at
  // RUNTIME in the live DB (source='household-onboarding', active=false, inserted via the
  // gated member INSERT policy). They are per-household provisional rows, NOT curated
  // catalog content, so they must NOT be materialised into committed repo files or the
  // parity snapshot — the parity test already excludes them by the same predicate
  // (tests/areas-db-repo-parity.test.js isOnboardingStub). Filtering here keeps the
  // materialiser and the test in lockstep: a member-added stub never becomes a repo file.
  const skipped = rows.filter((r) => r.data?.source === 'household-onboarding').map((r) => r.id);
  rows = rows.filter((r) => r.data?.source !== 'household-onboarding');
  const dumpIds = new Set(rows.map((r) => r.id));
  const written = [];
  const unchanged = [];

  for (const { id, data } of rows) {
    const rec = canonicalRecord(data);
    const next = json(rec);
    const path = resolve(AREAS_DIR, `${id}.json`);
    const cur = existsSync(path) ? await readFile(path, 'utf8') : null;
    let same = cur === next;
    if (!same && cur != null) {
      try { same = canonEqual(JSON.parse(cur), rec); } catch { same = false; }
    }
    if (same) { unchanged.push(id); continue; }   // semantically identical → keep existing file
    written.push(id);
    if (!dryRun) await writeFile(path, next);
  }

  // Prune: per-area files whose id is no longer a DB row (e.g. a migrated-away id).
  const removed = [];
  if (prune) {
    const diskIds = await readDiskIds();
    for (const id of diskIds) {
      if (dumpIds.has(id)) continue;
      removed.push(id);
      if (!dryRun) await unlink(resolve(AREAS_DIR, `${id}.json`));
    }
  }

  // Parity snapshot — sorted by id, only on change.
  const snap = rows.map((r) => snapshotRow(r.data)).sort((a, b) => a.id.localeCompare(b.id));
  const snapText = json(snap);
  let snapshotWritten = false;
  const curSnap = existsSync(SNAPSHOT) ? await readFile(SNAPSHOT, 'utf8') : null;
  if (curSnap !== snapText) { snapshotWritten = true; if (!dryRun) await writeFile(SNAPSHOT, snapText); }

  return { written, unchanged, removed, skipped, snapshotWritten, total: rows.length };
}

async function main() {
  const argv = process.argv.slice(2);
  const fromFileIdx = argv.indexOf('--from-file');
  const fromFile = fromFileIdx >= 0 ? argv[fromFileIdx + 1] : null;
  const prune = argv.includes('--prune');
  const dryRun = argv.includes('--dry-run');

  let raw;
  if (fromFile) {
    raw = JSON.parse(await readFile(resolve(fromFile), 'utf8'));
    console.log(`areas loaded from file: ${fromFile}`);
  } else {
    if (!SERVICE_KEY) throw new Error('no service key — dump via MCP and pass --from-file <json> (see header)');
    raw = await restGetAreas();
    console.log('areas loaded via REST');
  }

  const rows = normaliseDump(raw);
  console.log(`=== sync-areas-from-supabase ===`);
  console.log(`DB rows: ${rows.length} · prune: ${prune ? 'on' : 'off'}${dryRun ? ' · DRY-RUN' : ''}`);

  const r = await materialise(rows, { prune, dryRun });
  console.log(`\nwritten ${r.written.length} · unchanged ${r.unchanged.length} · removed ${r.removed.length} · skipped ${r.skipped.length} (household-onboarding) · snapshot ${r.snapshotWritten ? 'updated' : 'unchanged'}`);
  if (r.written.length) console.log(`  written: ${r.written.slice(0, 20).join(', ')}${r.written.length > 20 ? ` … (+${r.written.length - 20})` : ''}`);
  if (r.removed.length) console.log(`  removed (id no longer in DB): ${r.removed.join(', ')}`);
  if (r.skipped.length) console.log(`  skipped (household-onboarding stubs, not materialised): ${r.skipped.join(', ')}`);
  if (dryRun) console.log('\nDRY-RUN — nothing written.');
}

if (import.meta.url === pathToFileURL(process.argv[1] || '').href) {
  main().catch((e) => { console.error('SYNC CRASHED:', e); process.exit(1); });
}
